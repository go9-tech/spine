package tech.go9.spine.web.client.internal.configuration;

import org.springframework.boot.context.properties.EnableConfigurationProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.ComponentScan;
import org.springframework.context.annotation.Configuration;

import com.fasterxml.jackson.databind.Module;

import tech.go9.spine.web.client.api.jackson.PageModule;

@Configuration
@ComponentScan(SpineWebClientConstants.APP_BASE_PACKAGE)
@EnableConfigurationProperties(SpineWebClientProperties.class)
public class SpineWebClientConfiguration {

	@Bean
	Module pageModule() {
		return new PageModule();
	}

}
